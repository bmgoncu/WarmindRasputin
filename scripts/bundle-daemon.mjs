/**
 * Bundles the daemon into a single file so the app does not need a checked-out repo.
 *
 * Until now the overlay ran `lib/server/daemon.js` from a compile-time path, which exists only on
 * the machine that built it. This inlines the daemon and its two runtime dependencies (ws, the
 * Agent SDK) into one file that ships inside the bundle.
 *
 * Node itself is NOT bundled. A Node runtime is ~100 MB and the app already resolves an installed
 * one by absolute path; ffmpeg and rubberband stay external for the same reason, plus ffmpeg's
 * licence makes redistribution a decision rather than a detail. Those are prerequisites, checked
 * by scripts/setup.sh and reported at startup.
 */
import { build } from "esbuild";
import { mkdir, copyFile } from "node:fs/promises";

await mkdir("dist-daemon", { recursive: true });

const result = await build({
    entryPoints: ["lib/server/daemon.js"],
    bundle: true,
    platform: "node",
    format: "esm",
    target: "node22",
    outfile: "dist-daemon/daemon.mjs",
    banner: {
        js: [
            "// RasputinClaudeAI daemon — generated by scripts/bundle-daemon.mjs. Do not edit.",
            // ESM output has no `require`, but bundled CommonJS dependencies still call it at
            // runtime — the bundle died with "Dynamic require is not supported" without this shim.
            "import { createRequire as __cr } from 'node:module';",
            "const require = __cr(import.meta.url);",
        ].join("\n"),
    },
    // import.meta.url is used for the run-directly guard; ESM output preserves it correctly.
    metafile: true,
    logLevel: "error",
});

// The fitted EQ curve is data the voice chain reads at render time, not code.
await copyFile("assets/eq-curve.json", "dist-daemon/eq-curve.json");
// The pronunciation map is data too, and speech is noticeably worse without it.
await copyFile("assets/speech-map.json", "dist-daemon/speech-map.json");

const bytes = Object.values(result.metafile.outputs)[0]?.bytes ?? 0;
console.log(`  dist-daemon/daemon.mjs  ${(bytes / 1024 / 1024).toFixed(1)} MB`);
